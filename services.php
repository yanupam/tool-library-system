            <p>
              A tool library is an example of a Library of Things. Tool libraries allow patrons to check out or borrow
              tools, equipment and "how-to" instructional materials, functioning either as a rental shop, with a charge
              for borrowing the tools, or more commonly free of charge as a form of community sharing.
            </p>

            <p><strong>Why we need Tool Library for?</strong></p>

            <p>
              Save money, save space, save the environment
              And that’s the beauty of a tool library. Borrow a tool, use it for one afternoon at a fraction of the cost
              of buying it and instead of it cluttering up your house gathering dust, it’s back off to a busy life of
              chiselling all across town, week in week out (probably).
            </p>

            <p class="note note-light">
              <strong>Note:</strong> In 2009, the community of West Seattle in Washington started the West Seattle Tool
              Library, which provides a wide variety of tools and resources for individuals and organizations while
              specifically encouraging sustainable urban living. In 2011, Popular Mechanics recognized "Building a Local
              Tool Library" as one of its top ten ways to change the world, while highlighting the West Seattle Tool
              Library.
            </p>

            <p>
              Another early tool lending library was in Columbus, Ohio started in 1976. Originally run by the city, the
              tool library is now operated by ModCon Living, a non-profit organization that works to preserve and
              revitalize homes and communities in Central Ohio. The ModCon Living Tool Library makes available over
              4,500 tools free of charge to both individuals and non-profit organizations. This tool library was among
              many in what could be considered the first generation of tool libraries - including The Phinney Tool
              Library in 1978 and The Berkeley Tool Library in 1979 - most of which were founded in the late 70s or
              early 80s. Many of these libraries were started with community block grants. A variation of the tool
              lending library model exists in Atlanta, Georgia. At the Atlanta Community ToolBank, the tools are
              reserved for use only by nonprofits and other community-based organizations who are performing volunteer
              and facility maintenance projects. The ToolBank tool inventory is not available to individuals.
            </p>

